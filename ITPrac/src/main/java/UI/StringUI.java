    private void ContainDoubleLetterBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ContainDoubleLetterBtActionPerformed
        String input = InputField.getText();
        boolean out = StringUtility.hasDoubleLetter(input);
        if (out == true) {
            OutputTextField.setText("It has a double letter");
        } else {
            OutputTextField.setText("It does NOT have a double letetr");
        }

    }//GEN-LAST:event_ContainDoubleLetterBtActionPerformed

