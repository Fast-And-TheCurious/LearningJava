    private void PasswordStrBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PasswordStrBtActionPerformed
        String password = InputField.getText();
        boolean strength = StringUtility.isStrongPassword(password);
        if (strength = true) {
            OutputTextField.setText("Passwod is strong!");
        } else {
            OutputTextField.setText("Password is weak.");
        }
    }//GEN-LAST:event_PasswordStrBtActionPerformed

